CREATE DATABASE HR
USE HR
CREATE TABLE WORKERS
(
	ID INT IDENTITY(1,1) PRIMARY KEY,
	WORKERCODE NVARCHAR(20),
	WORKERNAME VARCHAR(100),
	GENDER VARCHAR(1),
	BIRTHDATE DATE,
	TCNO VARCHAR(11),
	WORKERBARCODE uniqueidentifier  
)
CREATE TABLE WORKERTRANSACTIONS
(
	ID  INT IDENTITY(1,1) PRIMARY KEY,
	WORKERID INT,
	DATE_ DATETIME,
	IOTYPE VARCHAR(1),
	GATEID INT
)

